    private void ListarClientes() {
        DefaultTableModel tabla = new DefaultTableModel();
        Data_Clientes objEmp = new Data_Clientes();
        ArrayList<Data_Clientes> lista = objEmp.ListaClientes();

        tabla.addColumn("Id Cliente");
        tabla.addColumn("Nombre Completo");
        tabla.addColumn("Correo");
        tabla.addColumn("Telefono");

        tabla.setRowCount(lista.size());
        int i = 0;
        for (Data_Clientes emp : lista) {
            tabla.setValueAt(emp.getIdCliente(), i, 0);
            tabla.setValueAt(emp.getNombreCompleto(), i, 1);
            tabla.setValueAt(emp.getCorreo(), i, 2);
            tabla.setValueAt(emp.getTelefono(), i, 3);            
            i++;
        }
        this.jTable1.setModel(tabla);
    }
        
