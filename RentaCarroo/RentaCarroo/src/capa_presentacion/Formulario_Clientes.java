    private void JBtEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtEliminarActionPerformed
        // TODO add your handling code here:
        try {
            JBtModificar.setEnabled(false);
            if (JtfidCliente.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Ingrese un ID para eliminar");
                return;
            }
            int confirm = JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar?");
            if (confirm == 0) {
                Data_Clientes objEmp = new Data_Clientes();
                objEmp.setIdCliente(Integer.parseInt(JtfidCliente.getText()));
                JOptionPane.showMessageDialog(null, objEmp.EliminarCliente());
                ListarClientes();
                LimpiarCajasTexto();
                JtfidCliente.setEnabled(true);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al eliminar");
        }
    }//GEN-LAST:event_JBtEliminarActionPerformed
                                       
